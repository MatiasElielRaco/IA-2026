nombre = "Pedro"

# Cadena de texto con concatenación
bienvenida = "Hola, " + nombre + " ¿Cómo estás?"

# Cadena de texto con f-strings
bienvenida = f"Hola, {nombre}. ¡Bienvenido al curso de Python!"


# Operadores de pertenencia
print("Pedro" not in bienvenida) #Resultado: false
print("Pedro" in bienvenida) #Resultado: true

# Python es un lenguaje sencible a mayúsculas y minúsculas